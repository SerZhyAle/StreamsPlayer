<# Prints the explicit manual release checklist. It changes no remote state. #>
[CmdletBinding()]
param()

$ErrorActionPreference = 'Stop'
$root = Split-Path $PSScriptRoot -Parent
Push-Location $root
try {
    $version = (Select-String -Path Directory.Build.props -Pattern '<Version>([^<]+)</Version>').Matches.Groups[1].Value
    if ($version -notmatch '^\d{2}\.\d{4}\.\d{4}$') { throw "Version '$version' must use YY.MMDD.HHmm." }
    [DateTime]::ParseExact($version, 'yy.MMdd.HHmm', [Globalization.CultureInfo]::InvariantCulture) | Out-Null
    $branch = git branch --show-current
    $dirty = git status --porcelain
    Write-Host "StreamPlayer release checklist for v$version" -ForegroundColor Cyan
    Write-Host "Branch: $branch"
    if ($dirty) { Write-Warning 'Working tree is not clean.' }
    Write-Host @"

1. Confirm all intended changes are committed on main and CI is green.
2. Run: ./scripts/check.ps1
3. Set a new UTC `YY.MMDD.HHmm` value in every version field in Directory.Build.props; update README.md, docs/index.html and msix/store-listing.md.
4. Build and locally test the package: ./msix/build-msix.ps1 -SelfSign
5. Create and push tag v$version only after explicit release approval.
6. Verify the GitHub Release zip and SHA256 generated by release.yml.
7. Copy a release-specific manifest from winget/templates/ to winget-pkgs,
   replace URL/hash/version, validate it, and submit its pull request.
8. Build an unsigned MSIX with the reserved Partner Center identity, upload it,
   update Store listing text/screenshots, then submit for certification.
9. Publish GitHub Pages only when the site is ready for public visibility.

This command does not tag, push, upload, submit, or publish anything.
"@
}
finally { Pop-Location }
