function setThemeMeta(theme) {
  const meta = document.querySelector('meta[name="theme-color"]');
  if (meta) {
    meta.content = theme === "light" ? "#eef3ea" : "#0a0f0a";
  }
}

function storePreference(key, value) {
  try {
    localStorage.setItem(key, value);
    return true;
  } catch (error) {
    return false;
  }
}

function readPreference(key) {
  try {
    return localStorage.getItem(key);
  } catch (error) {
    return null;
  }
}

function toggleTheme() {
  const nextTheme = document.documentElement.dataset.theme === "light" ? "dark" : "light";
  document.documentElement.dataset.theme = nextTheme;
  storePreference("streamsplayer-theme", nextTheme);
  setThemeMeta(nextTheme);
}

function setLanguage(language) {
  const languageKey = translations[language] ? language : "en";
  const copy = translations[languageKey];
  const page = document.body.dataset.page || "home";

  document.documentElement.lang = languageKey;
  document.documentElement.dataset.lang = languageKey;
  document.title = copy[`title-${page}`];

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.content = copy[`description-${page}`];
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = copy[element.dataset.i18n];
    if (value !== undefined) {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const value = copy[element.dataset.i18nAriaLabel];
    if (value !== undefined) {
      element.setAttribute("aria-label", value);
    }
  });

  document.querySelectorAll(".language-switcher button[data-language]").forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.language === languageKey));
  });

  storePreference("streamsplayer-lang", languageKey);
}

function resolveLanguage() {
  const stored = readPreference("streamsplayer-lang");
  if (translations[stored]) {
    return stored;
  }

  const browserLanguage = (navigator.language || "en").toLowerCase();
  if (browserLanguage.startsWith("ru")) {
    return "ru";
  }
  if (browserLanguage.startsWith("uk")) {
    return "uk";
  }
  return "en";
}

document.addEventListener("DOMContentLoaded", () => {
  setLanguage(resolveLanguage());
  setThemeMeta(document.documentElement.dataset.theme || "dark");

  const toTop = document.getElementById("toTop");
  if (toTop) {
    window.addEventListener("scroll", () => {
      toTop.classList.toggle("show", window.scrollY > 600);
    });
  }
});
